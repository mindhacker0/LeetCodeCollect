//向量
//数域:一些复数组成的集合,如果任意两个数的四则运算结果还属于这个集合,那么这个集合就称为数域
//向量:数域中n个元素组成的有序数组,称为数域上的一个n维向量
//向量相等:如果两个向量的分量都相等,那么这两个向量相等
//向量相加:向量A和B的和等于分量分别相加的和
//向量的数乘:向量A和k的数乘,等于向量的分量分别乘以k
//零向量:分量都为0的向量称为零向量
//负向量:向量A和-1的数乘记为-A,A-B定义为A+(-B)
//线性相关:如果存在不全为零的一组数k1...kn和向量组A1-An分别相乘的和为零，则向量组A1-An线性相关。
//单个零向量构成的向量组是线性相关的
//向量组A1-An线性相关的充要条件是其中至少有一个向量由其它向量线性表出。
//设向量组B1-Bn线性无关，B1-Bn，和a组合线性相关，则a能由向量组B1-Bn线性表出，反之亦然。
//向量等价:两个向量组互相可以线性表出，就称他们等价。
//部分线性相关则全局线性相关
//向量组线性无关，则部分向量也无关
//含有零向量的向量组必线性相关
//在r维向量组A1-An各个向量的都添加n-r个分量变成n维向量组B1-Bn。B1-Bn线性相关那么A1-An也线性相关。A1-An线性无关，B1-Bn也线性无关。
//n阶方阵A的行列式的值等于零的充分必要条件是A的行（列）向量线性相关
//n+1个n维向量必线性相关
//向量A1-An可以由B1-Bm表示出,且n>m那么A1-An线性相关。
//向量A1-An可以由B1-Bm表示出,且A1-An线性无关，那么n<=m              
//极大线性无关组：一个向量组的一个 部分组 称为一个极大线性无关组，如果这个部分组本身是线性无关的，并且从这向量组中向这部分组添加任何一个向量，所得的部分组都线性相关。
//一个向量组的一个部分组称为一个极大线性无关组，如果这个部分组本身是线性无关的，并且这向量组中任意向量可由这部分组线性表出
//一向量组的极大线性无关组与向量组本身等价
//一向量组的任意两个极大线性无关组都等价
//一向量组的极大线性无关组都含有相同个数的向量
//向量组的秩:向量组的极大线性无关组所含向量的个数称为这个向量组的秩。
//向量组任意线性无关的部分组都可扩充为一个极大线性无关组
//k级子式：在一个sxn的矩阵A中任意选定k行和k列，位于这些选定的行和列的交点上的k*k个元素按原来的次序所组成的k*k级矩阵的行列式，称为A的一个k级子式。
//设r<=n,n维向量组A1-Ar线性无关的充要条件是矩阵中存在一个行列式值不为0的r级子式，该子式所在的行和列就是行向量组和列向量组的一个极大线性无关组。
//设V为数域P上n维向量组成的集合，如果V非空，且对于向量加法和数乘运算封闭，就称集合V为数域P上的一个向量空间。
//最小向量空间:n维零向量所形成的集合{0}构成一个向量空间
//如果V1和V2都是向量空间，且V1属于V2，称V1是V2的子空间。
//任何由n维实向量所组成的向量空间都是R(n)的子空间，R(n)和{0}称为R(n)的平凡子空间，其它子空间称为R(n)的非平凡子空间。
//向量的基：设V为一个向量空间，如果V中的向量组A1-Ar满足：1.A1-Ar线性无关。2.V中的任意向量都可经A1-Ar线性表出，那么向量A1-Ar就称为V的一个基，r为V的维数，并称V为一个r维的向量空间。
//如果向量空间V没有基，就说V的维数为0，0维向量空间只含一个零向量。
//如果把向量空间V看作向量组，那么V的基就是它的极大线性无关组，V的维数就是它的秩，当V由n维向量组成时，它的维数不会超过n. 
//线性方程组有解的充分必要条件是系数矩阵和增广矩阵有相同的秩。·
//齐次线性方程组:如果一个线性方程的常数项都等于零，那么这个线性方程组就叫做齐次线性方程组。
//齐次线性方程组有非零解的充分必要条件是，系数矩阵的秩小于未知量的个数。
//基础解系：设a1-ar是齐次线性方程组的r个解向量，且a1-ar线性无关，方程组的任意一个解向量都能由a1-ar线性表出，则a1-ar为齐次线性方程组的一个基础解系;
//齐次线性方程组若有非零解，则它一定有基础解系，且基础解系所含解向量的个数等于n-r
//齐次线性方程组解的结构定理：齐次线性方程组若有非零解，则它的通解(齐次方程的全部解)就是基础解系的线性组合。 
//导出组（非齐次线性方程）：方程组的系数矩阵不全为零，称为非齐次线性方程组，如果把常数项都换成零就得到相应的齐次线性方程组，称它为非齐次线性方程的导出组。
//非齐次线性方程组解的结构定理：如果线性方程组有解，那么方程组的一个解与它的导出方程组的解之和是方程组的一个解，方程组的任意解都可以写成方程组的一个特解与它的导出方程组的解之和。